class netester:
    """
    netester类操作network io。
    """
# ----------------------------------------------------------------------------------------------------
    @staticmethod
    def client(config = {'connect':[{'type':'TCP','host':'127.0.0.1','port':10517}]}):
        for item in config['connect']:
            info = netester.encode(item)
            s = socket.socket(socket.AF_INET, info['type'])
            s.connect(info['addr'])
